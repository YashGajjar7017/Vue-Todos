<script setup lang="ts">
import { useStore } from 'vuex';
import { COLOR, TodoState } from '../store';

const props = defineProps<{ item: TodoState }>();

const store = useStore();

const changeColor = () => {
  const newState = {
    ...props.item,
    color: props.item.color === COLOR.BLACK ? COLOR.WHITE : COLOR.BLACK,
    noteColor: props.item.noteColor === COLOR.WHITE ? COLOR.BLACK : COLOR.WHITE,
  };
  store.dispatch('sync', newState);
};

const finishTodo = () => {
  const newState = {
    ...props.item,
    isFinish: !props.item.isFinish,
  };
  store.dispatch('sync', newState);
};

const deleteTodo = () => {
  store.dispatch('delete', props.item.id);
};
</script>
<template>
  <div class="self-end flex mt-2">
    <button @click="changeColor">
      <svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor">
        <path
          fill-rule="evenodd"
          d="M5 2a1 1 0 011 1v1h1a1 1 0 010 2H6v1a1 1 0 01-2 0V6H3a1 1 0 010-2h1V3a1 1 0 011-1zm0 10a1 1 0 011 1v1h1a1 1 0 110 2H6v1a1 1 0 11-2 0v-1H3a1 1 0 110-2h1v-1a1 1 0 011-1zM12 2a1 1 0 01.967.744L14.146 7.2 17.5 9.134a1 1 0 010 1.732l-3.354 1.935-1.18 4.455a1 1 0 01-1.933 0L9.854 12.8 6.5 10.866a1 1 0 010-1.732l3.354-1.935 1.18-4.455A1 1 0 0112 2z"
          clip-rule="evenodd"
        />
      </svg>
    </button>
    <button @click="finishTodo">
      <svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor">
        <path
          fill-rule="evenodd"
          d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
          clip-rule="evenodd"
        />
      </svg>
    </button>
    <button @click="deleteTodo">
      <svg xmlns="http://www.w3.org/2000/svg" class="h-5 w-5" viewBox="0 0 20 20" fill="currentColor">
        <path
          fill-rule="evenodd"
          d="M4.293 4.293a1 1 0 011.414 0L10 8.586l4.293-4.293a1 1 0 111.414 1.414L11.414 10l4.293 4.293a1 1 0 01-1.414 1.414L10 11.414l-4.293 4.293a1 1 0 01-1.414-1.414L8.586 10 4.293 5.707a1 1 0 010-1.414z"
          clip-rule="evenodd"
        />
      </svg>
    </button>
  </div>
</template>
